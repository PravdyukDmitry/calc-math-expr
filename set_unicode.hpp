#ifndef PCHOLA_SET_UNICODE_HPP
 #define PCHOLA_SET_UNICODE_HPP

#include <boost/program_options/detail/utf8_codecvt_facet.hpp>

namespace pchola {

void set_unicode ()
{
	using namespace boost::program_options::detail;
	using std::locale;

	setlocale (LC_ALL, "");

	typedef wchar_t ucs4_t;

	locale old_locale
		, utf8_locale (old_locale
			, new utf8_codecvt_facet);

	global (utf8_locale); // Set a New global locale
};

}; /// end namespace pchola

#endif /// PCHOLA_SET_UNICODE_HPP